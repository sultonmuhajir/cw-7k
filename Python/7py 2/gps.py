def gps(s, x):
   return 0 if len(x) <= 1 else max([3600*(x[i+1]-x[i])/s for i in range(len(x)-1)]) // 1


def gps(s, x):
   return max([(n-m)*3600/s for (m,n) in zip(x,x[1:])])


def gps(s, x):
   return int(max(map(lambda a: (a[1]-a[0])/(s/3600.0), zip(x,x[1:])))) if len(x) > 1 else 0


